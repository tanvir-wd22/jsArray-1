// add elements at the end of an array
const numbers = [22, 3, 7, 88, 8,2];
console.log(numbers);
numbers.push(333, 444);
console.log(numbers);

const bikes = ['yamaha', 'suzuki', 'toyota'];
console.log(bikes);
bikes.push('kawasaki', 'bmw');
console.log(bikes);

// remove elements at the end of an array
const students = [22, 44, 88, 85, 99];
console.log(students);
students.pop();
console.log(students);

